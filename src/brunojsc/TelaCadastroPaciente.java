    private void btnSalvarAlteracoesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarAlteracoesActionPerformed
        
        if(txtNomePaciente.getText().trim().isEmpty() ||txtCpfPaciente.getText().trim().isEmpty() || 
           txtIdadePaciente.getText().trim().isEmpty()) 
        {
        
            JOptionPane.showMessageDialog(rootPane, "Por favor preencha todos os campos!");
        
        }
        else 
        {
            Paciente paciente = new Paciente();
            
            if(!txtIdadePaciente.getText().isEmpty())
            
            {
                
                paciente.setIdpaciente(Integer.parseInt(txtIdPaciente.getText()));
                
            }
            
         paciente.setCpf(txtCpfPaciente.getText().trim());
         paciente.setNome(txtNomePaciente.getText().trim());
         paciente.setIdade(Integer.parseInt(txtIdadePaciente.getText().trim()));
         
         String msg;
         if(paciente.getIdpaciente()== 0 )
         {
         
             msg = pacienteDAO.CadastratarPaciente(paciente);
             
             if(msg.equals("1"))
             {
             
                 msg = "PACIENTE CADASTRADO COM SUCESSO!";
                 
             }
             
             JOptionPane.showMessageDialog(rootPane, msg);
         }
         else
         {
             
             msg = pacienteDAO.AtualizarPaciente(paciente);
             if(msg.equals("1"))
             {
             
                 msg = "ALTERAÇÕES SALVAS COM SUCESSO!";
                 
             }
             JOptionPane.showMessageDialog(rootPane, msg);
             
         }
         this.limparCampos();
         this.preencherTabela();
         
        }
        
    }//GEN-LAST:event_btnSalvarAlteracoesActionPerformed

