    private void btnExcluirPacienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirPacienteActionPerformed
        
         int resposta = JOptionPane.showConfirmDialog(rootPane, "Deseja realmente excluir este usuário?");
        
        if(resposta == 0)
        {
        
        String idpaciente = txtIdPaciente.getText();
        String msg = pacienteDAO.ExcluirPaciente(idpaciente);
        
        if(msg.equals("1"))       
        {
        
        msg = "Usuário Excluído com Sucesso!";
        
        }
          
        JOptionPane.showMessageDialog(rootPane,msg);
        this.limparCampos();
        this.preencherTabela();
        }
        
    }//GEN-LAST:event_btnExcluirPacienteActionPerformed

